package beans;

public class Form {
	String Page;
	boolean Redirect;
	
	public String getPage() {
		return Page;
	}
	public void setPage(String page) {
		Page = page;
	}
	public boolean isRedirect() {
		return Redirect;
	}
	public void setRedirect(boolean redirect) {
		Redirect = redirect;
	}
	
}
